package com.MobileBankingSystem.account;

public class Account_Info {
	private int account_no;
	private int account_pwd;
	private int account_total_amount;
	
	public int getAccount_no() {
		return account_no;
	}
	public void setAccount_no(int account_no) {
		this.account_no = account_no;
	}
	public int getAccount_pwd() {
		return account_pwd;
	}
	public void setAccount_pwd(int account_pwd) {
		this.account_pwd = account_pwd;
	}
	public int getAccount_total_amount() {
		return account_total_amount;
	}
	public void setAccount_total_amount(int account_total_amount) {
		this.account_total_amount = account_total_amount;
	}
}
