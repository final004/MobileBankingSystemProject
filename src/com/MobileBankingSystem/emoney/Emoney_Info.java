package com.MobileBankingSystem.emoney;

public class Emoney_Info {
	private int emoney_price;
	private int emoney_volume;
	private int emoney_total_amount;
	
	public int getEmoney_price() {
		return emoney_price;
	}
	public void setEmoney_price(int emoney_price) {
		this.emoney_price = emoney_price;
	}
	public int getEmoney_volume() {
		return emoney_volume;
	}
	public void setEmoney_volume(int emoney_volume) {
		this.emoney_volume = emoney_volume;
	}
	public int getEmoney_total_amount() {
		return emoney_total_amount;
	}
	public void setEmoney_total_amount(int emoney_total_amount) {
		this.emoney_total_amount = emoney_total_amount;
	}
	
}
